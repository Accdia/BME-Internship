/* 串口接收缓冲区非空中断的回调函数:会主动关闭中断 */
void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
    if (huart == &huart1)
    {
        HAL_UART_Receive_IT(&huart1, &tmp, 1);
        if (tmp == '\n')     /* 判断数据结束符 */
        {
            g_ucUart1RxBuf[g_ucUart1RxCnt-1] = '\0'; /* 将数据转为字符串 */
            g_ucUart1RxFlag = 1;
        }
        else 
        {
            g_ucUart1RxBuf[g_ucUart1RxCnt] = tmp;
            g_ucUart1RxCnt++;
        }
    }

}

